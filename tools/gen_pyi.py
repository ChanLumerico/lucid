def gen_engine_pyi() -> str:
    from lucid._C import engine as e

    # Build amp policy map from the op registry
    policy_map: dict[str, str] = {}
    try:
        for schema in e.op_registry_all():
            policy_map[schema.name] = str(schema.amp_policy)
    except Exception:
        pass

    # All callable non-class members (excludes classes like Dtype, TensorImpl, etc.)
    skip_classes = {x for x in dir(e) if isinstance(getattr(e, x, None), type)}
    skip_fixed = {
        "amp_is_active", "amp_active_dtype", "set_deterministic", "is_deterministic",
        "op_registry_all", "op_registry_size", "op_lookup", "engine_backward",
        "no_grad", "is_grad_enabled", "ABI_VERSION",
    }
    names = sorted(
        x for x in dir(e)
        if not x.startswith("_")
        and x not in skip_classes
        and x not in skip_fixed
        and callable(getattr(e, x, None))
    )

    lines = []
    for name in names:
        comment = _amp_policy_comment(name, policy_map)
        if comment:
            lines.append(f"def {name}(*args: Any, **kwargs: Any) -> TensorImpl:{comment}")
            lines.append("    ...")
            lines.append("")
        else:
            lines.append(f"def {name}(*args: Any, **kwargs: Any) -> TensorImpl: ...")

    # Count for summary
    count = len(names)
    return _ENGINE_HEADER + "\n".join(lines) + "\n", count


