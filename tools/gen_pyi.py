#!/usr/bin/env python3
"""Lucid stub generator — emits .pyi files for IDE type checking.

All stubs are derived from live sources; nothing is hardcoded:

  lucid/_C/engine.pyi       — pybind11 introspection of the built engine
  lucid/_tensor/tensor.pyi  — AST of tensor.py + nested defs in _dunders.py /
                               _to.py + registry-injected ops
  lucid/__init__.pyi         — AST of _factories/ + registry free functions
                               + static module-level symbols (dtypes etc.)

Usage:
    python tools/gen_pyi.py [--out-engine PATH] [--out-tensor PATH]
                             [--out-init PATH] [--dry-run]

Adding a new method to tensor.py, a new factory, or a new registry op will
automatically appear in the next stub generation without editing this file.
"""

from __future__ import annotations

import argparse
import ast
import inspect
import sys
import textwrap
from pathlib import Path
from typing import Any, NamedTuple

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

# ── GENERATED FILE BANNER ────────────────────────────────────────────────────

_BANNER = """\
# --------------------------------------------------------------------- #
# {filename} — auto-generated by tools/gen_pyi.py
# DO NOT EDIT — regenerate with:  python tools/gen_pyi.py
# --------------------------------------------------------------------- #

"""

# ── AST UTILITIES ─────────────────────────────────────────────────────────────


class MethodStub(NamedTuple):
    name: str
    sig: str            # full signature string, e.g. "(self, x: int = 0) -> Tensor"
    decorators: list[str]   # e.g. ["property", "classmethod"]
    is_setter: bool = False


def _ann(node: ast.expr | None) -> str:
    """Return annotation string, or empty string if absent."""
    return ast.unparse(node) if node is not None else ""


def _default(node: ast.expr | None) -> str:
    return ast.unparse(node) if node is not None else ""


def _param_list(args: ast.arguments) -> list[str]:
    """Reconstruct the parameter list from an ast.arguments node."""
    params: list[str] = []
    n_args = len(args.args)
    n_defaults = len(args.defaults)

    for i, arg in enumerate(args.args):
        ann = _ann(arg.annotation)
        p = f"{arg.arg}: {ann}" if ann else arg.arg
        default_idx = i - (n_args - n_defaults)
        if default_idx >= 0 and args.defaults[default_idx] is not None:
            p += f" = {_default(args.defaults[default_idx])}"
        params.append(p)

    if args.vararg:
        va = args.vararg
        ann = _ann(va.annotation)
        params.append(f"*{va.arg}: {ann}" if ann else f"*{va.arg}")
    elif args.kwonlyargs:
        params.append("*")

    for i, arg in enumerate(args.kwonlyargs):
        ann = _ann(arg.annotation)
        p = f"{arg.arg}: {ann}" if ann else arg.arg
        if args.kw_defaults[i] is not None:
            p += f" = {_default(args.kw_defaults[i])}"
        params.append(p)

    if args.kwarg:
        kw = args.kwarg
        ann = _ann(kw.annotation)
        params.append(f"**{kw.arg}: {ann}" if ann else f"**{kw.arg}")

    return params


def _fn_sig(node: ast.FunctionDef | ast.AsyncFunctionDef) -> str:
    """Return '(params) -> ReturnType' for a function AST node."""
    params = _param_list(node.args)
    ret = _ann(node.returns)
    body = ", ".join(params)
    return f"({body}) -> {ret}" if ret else f"({body})"


def _decorator_names(node: ast.FunctionDef | ast.AsyncFunctionDef) -> list[str]:
    """Return plain decorator names (e.g. 'property', 'classmethod')."""
    names: list[str] = []
    for d in node.decorator_list:
        if isinstance(d, ast.Name):
            names.append(d.id)
        elif isinstance(d, ast.Attribute):
            names.append(f"{ast.unparse(d)}")
    return names


# ── SOURCE EXTRACTORS ─────────────────────────────────────────────────────────


def extract_class_stubs(source: Path, class_name: str) -> list[MethodStub]:
    """Extract public method stubs from a class definition in a source file."""
    tree = ast.parse(source.read_text(encoding="utf-8"))
    stubs: list[MethodStub] = []

    for node in ast.walk(tree):
        if not (isinstance(node, ast.ClassDef) and node.name == class_name):
            continue
        for item in node.body:
            if not isinstance(item, (ast.FunctionDef, ast.AsyncFunctionDef)):
                continue
            if item.name.startswith("__") and item.name.endswith("__"):
                # Keep dunder only if it's a well-known one we want to expose
                if item.name not in {
                    "__init__", "__repr__", "__str__", "__len__", "__bool__",
                    "__iter__", "__getitem__", "__setitem__", "__format__",
                    "__hash__", "__contains__", "__class_getitem__",
                }:
                    continue
            decs = _decorator_names(item)
            is_setter = "setter" in " ".join(decs) or any(
                isinstance(d, ast.Attribute) and d.attr == "setter"
                for d in item.decorator_list
            )
            stubs.append(MethodStub(
                name=item.name,
                sig=_fn_sig(item),
                decorators=decs,
                is_setter=is_setter,
            ))
        break  # found the class, stop walking

    return stubs


def extract_nested_fns(source: Path, outer_fn: str) -> list[MethodStub]:
    """Extract stubs for inner `def` nodes inside a named outer function.

    Used to pull out the closures from _inject_dunders() and _inject_to().
    """
    tree = ast.parse(source.read_text(encoding="utf-8"))
    stubs: list[MethodStub] = []

    for node in ast.walk(tree):
        if not (isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef))
                and node.name == outer_fn):
            continue
        for item in node.body:
            if isinstance(item, (ast.FunctionDef, ast.AsyncFunctionDef)):
                stubs.append(MethodStub(
                    name=item.name,
                    sig=_fn_sig(item),
                    decorators=_decorator_names(item),
                ))
        break

    return stubs


def extract_module_fns(source: Path, *, skip_private: bool = True) -> list[MethodStub]:
    """Extract top-level function stubs from a module."""
    tree = ast.parse(source.read_text(encoding="utf-8"))
    stubs: list[MethodStub] = []

    for node in tree.body:
        if not isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            continue
        if skip_private and node.name.startswith("_"):
            continue
        stubs.append(MethodStub(
            name=node.name,
            sig=_fn_sig(node),
            decorators=_decorator_names(node),
        ))

    return stubs


# ── STUB EMITTERS ─────────────────────────────────────────────────────────────


def _emit_method(stub: MethodStub, indent: int = 4) -> list[str]:
    """Emit the stub lines for a single method/function."""
    pad = " " * indent
    lines: list[str] = []
    for dec in stub.decorators:
        # Skip implementation-detail decorators
        if dec in {"overload"}:
            lines.append(f"{pad}@{dec}")
        elif dec == "property":
            lines.append(f"{pad}@{dec}")
        elif dec == "classmethod":
            lines.append(f"{pad}@{dec}")
        elif dec == "staticmethod":
            lines.append(f"{pad}@{dec}")
        elif stub.is_setter:
            lines.append(f"{pad}@{stub.name}.setter")
    lines.append(f"{pad}def {stub.name}{stub.sig}: ...")
    return lines


