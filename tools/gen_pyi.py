def _sig_from_ast(name: str, node: "ast.FunctionDef | ast.AsyncFunctionDef") -> str:  # type: ignore[name-defined]
    """Emit a stub line from a parsed AST ``FunctionDef`` node.

    Annotation nodes are converted to strings via ``ast.unparse`` (never
    evaluated), so TYPE_CHECKING-only names work without triggering
    ``NameError``.  Positional-only and keyword-only markers are preserved.
    ``*args`` / ``**kwargs`` are omitted per H9 unless the parameter name
    implies genuine variadic intent (e.g. ``*tensors``).
    """
    import ast

    args = node.args

    def _ann(ann_node: "ast.expr | None") -> str:
        if ann_node is None:
            return ""
        return ast.unparse(ann_node)

    parts: list[str] = []

    # Positional-only (before the ``/`` marker)
    n_posonly = len(args.posonlyargs)
    all_pos = list(args.posonlyargs) + list(args.args)
    n_pos_kw = len(args.args)
    n_all = n_posonly + n_pos_kw
    n_defaults = len(args.defaults)
    default_offset = n_all - n_defaults

    for i, arg in enumerate(all_pos):
        ann = _ann(arg.annotation)
        ptype = ann if ann else _PARAM_TYPE_MAP.get(arg.arg, "object")
        sfx = " = ..." if i >= default_offset else ""
        parts.append(f"{arg.arg}: {ptype}{sfx}")
    if n_posonly:
        parts.append("/")

    # *args — only emit when genuinely variadic (e.g. ``*tensors: Tensor``).
    # Per H9: ``*args: object`` is never emitted.
    if args.vararg:
        vname = args.vararg.arg
        ann = _ann(args.vararg.annotation)
        ptype = ann if ann else _PARAM_TYPE_MAP.get(vname, "object")
        # Emit only when the annotation or name is Tensor-typed (truly variadic).
        if ptype not in {"object", "Any"}:
            parts.append(f"*{vname}: {ptype}")
        elif args.kwonlyargs:
            parts.append("*")
    elif args.kwonlyargs:
        parts.append("*")

    # Keyword-only args
    kw_defaults = args.kw_defaults  # list of default AST nodes (None = no default)
    for i, arg in enumerate(args.kwonlyargs):
        ann = _ann(arg.annotation)
        ptype = ann if ann else _PARAM_TYPE_MAP.get(arg.arg, "object")
        sfx = " = ..." if kw_defaults[i] is not None else ""
        parts.append(f"{arg.arg}: {ptype}{sfx}")

    # **kwargs — H9: omit entirely.

    # Return type
    ret = _ann(node.returns) if node.returns else _RETURN_TYPE_MAP.get(name, "Tensor")

    return f"def {name}({', '.join(parts)}) -> {ret}: ..."


def _inferred_param_type(name: str, kind) -> str:
    """Stub annotation for a parameter whose runtime annotation is empty.

    Mirrors ``_annotation_for_param`` but without an OpEntry — used by the
    composite / alias paths in ``gen_init_pyi``.
    """
    import inspect as _inspect

    if kind == _inspect.Parameter.VAR_POSITIONAL:
        if name in {"shape", "sizes", "size", "dims"}:
            return "int"
        return "object"
    if kind == _inspect.Parameter.VAR_KEYWORD:
        return "object"
    return _PARAM_TYPE_MAP.get(name, "object")


def _simplify_annotation(ann: object) -> str:
    """Convert a runtime annotation object to a terse string for stubs."""
    import inspect

    if ann is inspect.Parameter.empty:
        return "Any"
    raw = str(ann)
    # Strip module prefixes that would be unknown inside the stub.
    raw = raw.replace("<class '", "").replace("'>", "")
    raw = raw.replace("typing.", "").replace("lucid._tensor.tensor.", "")
    raw = raw.replace("lucid._dtype.", "").replace("lucid._device.", "")
    # Collapse common generics to something valid in stubs.
    raw = raw.replace("NoneType", "None")
    return raw


def gen_init_pyi() -> tuple[str, int]:
    import inspect
    from lucid._ops._registry import _REGISTRY

    # Preserve any manually-written docstrings in the existing __init__.pyi.
    # Without this, each regeneration would wipe out hand-crafted NumPy-style
    # docstrings that the docs site (web/) relies on for the lucid.ops /
    # lucid.creation / lucid.ops.composite pages.
    _existing_docs = _load_existing_docstrings(ROOT / "lucid/__init__.pyi")

    # Names whose signatures are hand-crafted in the ``overrides`` list below.
    # Exclude them from the registry loop so the correct stub wins (registry
    # introspection yields pybind11 TensorImpl signatures, not Tensor).
    _OVERRIDE_NAMES: frozenset[str] = frozenset(
        {
            "sum",
            "mean",
            "prod",
            "max",
            "min",
            "var",
            "std",
            "argmax",
            "argmin",
            "squeeze",
            "repeat",
            "repeat_interleave",
            "split",
            "tensordot",
            "meshgrid",
        }
    )

    seen: set[str] = set()
    lines = []
    count = 0
    for entry in _REGISTRY:
        fn = entry.free_fn_name
        if fn is None or fn in seen or fn in _OVERRIDE_NAMES:
            continue
        seen.add(fn)
        lines.append(_format_with_docstring(_free_fn_sig(entry), fn, _existing_docs))
        count += 1

    # Also include reference-compatible overrides defined directly in _ops/__init__.py.
    # Uses _int / _bool aliases (defined at top of _INIT_HEADER) to avoid shadowing
    # by lucid's own int / bool dtype aliases.
    overrides = [
        "def sum(x: Tensor, dim: _int | list[_int] | None = None, keepdim: _bool = False, *, correction: _int = 1) -> Tensor: ...",
        "def mean(x: Tensor, dim: _int | list[_int] | None = None, keepdim: _bool = False) -> Tensor: ...",
        "def prod(x: Tensor, dim: _int | list[_int] | None = None, keepdim: _bool = False) -> Tensor: ...",
        "def max(x: Tensor, dim: _int | list[_int] | None = None, keepdim: _bool = False) -> Tensor: ...",
        "def min(x: Tensor, dim: _int | list[_int] | None = None, keepdim: _bool = False) -> Tensor: ...",
        "def var(x: Tensor, dim: _int | list[_int] | None = None, keepdim: _bool = False, *, correction: _int = 1) -> Tensor: ...",
        "def std(x: Tensor, dim: _int | list[_int] | None = None, keepdim: _bool = False, *, correction: _int = 1) -> Tensor: ...",
        "def argmax(x: Tensor, dim: _int | None = None, keepdim: _bool = False) -> Tensor: ...",
        "def argmin(x: Tensor, dim: _int | None = None, keepdim: _bool = False) -> Tensor: ...",
        "def squeeze(x: Tensor, dim: _int | list[_int] | None = None) -> Tensor: ...",
        "def repeat(x: Tensor, repeats: _int, dim: _int | None = None) -> Tensor: ...",
        "def repeat_interleave(x: Tensor, repeats: _int, dim: _int | None = None) -> Tensor: ...",
        "def split(x: Tensor, split_size_or_sections: _int | list[_int], dim: _int = 0) -> list[Tensor]: ...",
        "def tensordot(a: Tensor, b: Tensor, dims: _int | list[list[_int]] = 2) -> Tensor: ...",
        "def meshgrid(*tensors: Tensor, indexing: str = 'ij') -> list[Tensor]: ...",
    ]

    # ── Composite ops (lucid._ops.composite) ─────────────────────────────────
    import lucid._ops.composite as _comp
    from lucid._ops.composite import COMPOSITE_NAMES

    composite_lines = [
        "\n# ── Constants ────────────────────────────────────────────────────────",
        "pi: float",
        "e: float",
        "inf: float",
        "nan: float",
        "newaxis: None",
        "\n# ── Composite ops (elementwise / shape / blas / reductions / predicates / dtype) ─",
    ]
    _CONSTANT_NAMES = {"pi", "e", "inf", "nan", "newaxis"}
    for n in sorted(COMPOSITE_NAMES - _CONSTANT_NAMES):
        if n in seen:
            continue
        fn = getattr(_comp, n, None)
        if fn is None or not callable(fn):
            continue
        seen.add(n)
        composite_lines.append(
            _format_with_docstring(_sig_from_callable(n, fn), n, _existing_docs)
        )

    # ── nn.functional / linalg / method aliases ───────────────────────────────
    import lucid

    alias_groups = {
        "Tensor-method aliases": lucid._METHOD_ALIASES,  # type: ignore[attr-defined]
    }
    alias_lines = []
    for group_label, name_set in alias_groups.items():
        group: list[str] = []
        for n in sorted(name_set):
            if n in seen:
                continue
            fn = getattr(lucid, n, None)
            if fn is None:
                continue
            seen.add(n)
            group.append(
                _format_with_docstring(_sig_from_callable(n, fn), n, _existing_docs)
            )
        if group:
            alias_lines.append(f"\n# ── {group_label} ─────────────────")
            alias_lines.extend(group)

    # Attach preserved docstrings to the hand-crafted overrides too (sum,
    # mean, prod, var, std, argmax, argmin, squeeze, repeat, split, ...).
    overrides = [
        _format_with_docstring(
            line, line.split("(")[0].removeprefix("def ").strip(), _existing_docs
        )
        for line in overrides
    ]

    content = _INIT_HEADER + "\n".join(lines) + "\n\n"
    content += (
        "# ── Reference-compatible overrides (see _ops/__init__.py) ───────────\n"
    )
    content += "\n".join(overrides) + "\n"
    content += "\n".join(composite_lines) + "\n"
    content += "\n".join(alias_lines) + "\n"
    # _DType is a private alias (= dtype) used in source annotations but not
    # exported in __init__.pyi — replace with the public ``dtype`` name.
    content = content.replace("_DType", "dtype")

    total = count + len(
        [l for l in composite_lines + alias_lines if l.startswith("def ")]
    )
    return content, total


