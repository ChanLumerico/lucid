def _format_with_docstring(
    sig_line: str, fn_name: str, doc_map: dict[str, tuple[str, bool]]
) -> str:
    """Attach a preserved docstring (if any) to a ``def foo(...) -> R: ...`` line.

    If ``doc_map`` has an entry for ``fn_name``, emit the multi-line form:

        def foo(...) -> R:
            r\"\"\"...preserved docstring...\"\"\"
            ...

    Otherwise return the single-line form unchanged.  ``doc_map`` values are
    ``(docstring_text, is_raw_string)`` tuples — when ``is_raw_string=True``
    the docstring is emitted with the ``r`` prefix so LaTeX backslashes
    survive.
    """
    if fn_name not in doc_map:
        return sig_line
    doc_text, is_raw = doc_map[fn_name]
    # sig_line ends with ': ...'; strip the trailing ' ...' and add body.
    if sig_line.endswith(": ..."):
        head = sig_line[:-4]  # keep the colon
    else:
        head = sig_line
    indent = "    "
    prefix = "r" if is_raw else ""
    # Re-indent the docstring body
    body_lines = [indent + l for l in doc_text.split("\n")]
    body = "\n".join(body_lines)
    return f'{head}\n{indent}{prefix}"""{body}\n{indent}"""\n{indent}...'


def _load_existing_docstrings(path) -> dict[str, tuple[str, bool]]:
    """Parse an existing ``.pyi`` file and return ``{fn_name: (doc, is_raw)}``.

    Preserves manually-written docstrings across regenerations of the stub.
    Detects raw-string prefixes by re-scanning the source text (AST loses
    that information).
    """
    import ast as _ast
    import re as _re

    try:
        src = path.read_text(encoding="utf-8")
    except Exception:
        return {}
    try:
        tree = _ast.parse(src)
    except Exception:
        return {}
    # Map (line of docstring constant) → was-raw-prefixed?
    raw_at_line: dict[int, bool] = {}
    for m in _re.finditer(r'^[ \t]*([rR])?"""', src, _re.MULTILINE):
        # convert byte offset to line number (1-based)
        line = src.count("\n", 0, m.start()) + 1
        raw_at_line[line] = bool(m.group(1))
    out: dict[str, tuple[str, bool]] = {}
    for node in _ast.walk(tree):
        if not isinstance(node, _ast.FunctionDef):
            continue
        doc = _ast.get_docstring(node)
        if not doc:
            continue
        # Body's first stmt is the docstring Expr; check its line for raw-ness
        first = node.body[0]
        is_raw = raw_at_line.get(first.lineno, False)
        out[node.name] = (doc, is_raw)
    return out


def _free_fn_sig(entry) -> str:
    """Generate a free function signature for a registry entry.

    Pulls the explicit ``__signature__`` that ``lucid._ops._make_free_fn``
    attaches to every wrapper, so the stub mirrors the runtime surface
    without us having to redo all the parsing here.  Falls back to the
    legacy heuristic only if the wrapper hasn't been built yet (e.g.
    during a partial registry).
    """
    import inspect as _inspect

    name = entry.free_fn_name or entry.name

    # Live-introspect the actual wrapper.  This includes Tensor unwrapping
    # signatures + any param renames done in ``_rename_leading_tensor_params``.
    try:
        from lucid._ops import _make_free_fn

        wrapper = _make_free_fn(name)
        sig = _inspect.signature(wrapper)
    except Exception:
        sig = None

    if sig is not None:
        parts = []
        for pname, param in sig.parameters.items():
            kind = param.kind
            ann = _annotation_for_param(pname, param.annotation, entry, kind)
            ann_str = f": {ann}" if ann else ""
            if kind == _inspect.Parameter.VAR_POSITIONAL:
                parts.append(f"*{pname}{ann_str}")
            elif kind == _inspect.Parameter.VAR_KEYWORD:
                parts.append(f"**{pname}{ann_str}")
            elif kind == _inspect.Parameter.KEYWORD_ONLY:
                # Suppress redundant ``*`` if a VAR_POSITIONAL ``*xxx`` is
                # already present — Python's grammar disallows two ``*``
                # markers in one signature.
                if not any(p == "*" for p in parts) and not any(
                    p.startswith("*") and not p.startswith("**") for p in parts
                ):
                    parts.append("*")
                if param.default is _inspect.Parameter.empty:
                    parts.append(f"{pname}{ann_str}")
                else:
                    parts.append(f"{pname}{ann_str} = ...")
            else:  # POSITIONAL_OR_KEYWORD / POSITIONAL_ONLY
                if param.default is _inspect.Parameter.empty:
                    parts.append(f"{pname}{ann_str}")
                else:
                    parts.append(f"{pname}{ann_str} = ...")
        return f"def {name}({', '.join(parts)}) -> Tensor: ..."

    # Fallback (shouldn't trigger in normal builds): legacy heuristic.
    n = entry.n_tensor_args
    if n == -1:
        return f"def {name}(tensors: list[Tensor], *args: object) -> Tensor: ..."
    parts = []
    if n >= 1:
        parts.append("input: Tensor")
    if n >= 2:
        parts.append("other: Tensor | float")
    if entry.extra_kwargs:
        parts.append("*args: Any")
        parts.append("**kwargs: Any")
    return f"def {name}({', '.join(parts)}) -> Tensor: ..."


