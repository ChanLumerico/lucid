def generate_init_pyi(registry: list[Any], out_path: Path) -> None:
    creation_src = ROOT / "lucid" / "_factories" / "creation.py"
    random_src   = ROOT / "lucid" / "_factories" / "random.py"

    lines: list[str] = [_BANNER.format(filename="__init__.pyi"), _INIT_IMPORTS, "",
                         _INIT_STATIC, ""]

    # ── factory functions from _factories/creation.py ─────────────────────────
    lines.append("# ── Tensor factories — deterministic (creation.py) ───────────────────────")
    for stub in extract_module_fns(creation_src):
        lines.append(f"def {stub.name}{stub.sig}: ...")
    lines.append("")

    # ── random factories from _factories/random.py ────────────────────────────
